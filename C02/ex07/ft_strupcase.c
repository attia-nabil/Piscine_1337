#include <unistd.h>

char	*ft_strupcase(char *str)
{
	char	*ptr;

	ptr = str;
	while (*str != '\0')
	{
		if (*str >= 97 && *str <= 122 )
		{
			*str -= 32;
		}
		str++;
	}
	return (ptr);
}
